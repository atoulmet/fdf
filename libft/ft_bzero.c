#include "libft.h"

void		ft_bzero(void *s, size_t n)
{
	char			*t;
	size_t			i;

	t = (char *)s;
	i = 0;
	if (n == 0)
		return ;
	while (i < n)
	{
		*t++ = '\0';
		i++;
	}
}
